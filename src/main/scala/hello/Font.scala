package hello

/** A tiny embedded 5x7 bitmap font.
  *
  * Each glyph is seven rows of five cells; `#` marks a lit pixel. Enough of
  * the alphabet to spell anything you'd want to shout at a terminal.
  */
object Font:
  val Height = 7
  val Width  = 5

  private val glyphs: Map[Char, Vector[String]] = Map(
    'A' -> Vector(" ### ", "#   #", "#   #", "#####", "#   #", "#   #", "#   #"),
    'B' -> Vector("#### ", "#   #", "#   #", "#### ", "#   #", "#   #", "#### "),
    'C' -> Vector(" ####", "#    ", "#    ", "#    ", "#    ", "#    ", " ####"),
    'D' -> Vector("#### ", "#   #", "#   #", "#   #", "#   #", "#   #", "#### "),
    'E' -> Vector("#####", "#    ", "#    ", "#### ", "#    ", "#    ", "#####"),
    'F' -> Vector("#####", "#    ", "#    ", "#### ", "#    ", "#    ", "#    "),
    'G' -> Vector(" ####", "#    ", "#    ", "#  ##", "#   #", "#   #", " ####"),
    'H' -> Vector("#   #", "#   #", "#   #", "#####", "#   #", "#   #", "#   #"),
    'I' -> Vector("#####", "  #  ", "  #  ", "  #  ", "  #  ", "  #  ", "#####"),
    'J' -> Vector("#####", "   # ", "   # ", "   # ", "   # ", "#  # ", " ##  "),
    'K' -> Vector("#   #", "#  # ", "# #  ", "##   ", "# #  ", "#  # ", "#   #"),
    'L' -> Vector("#    ", "#    ", "#    ", "#    ", "#    ", "#    ", "#####"),
    'M' -> Vector("#   #", "## ##", "# # #", "# # #", "#   #", "#   #", "#   #"),
    'N' -> Vector("#   #", "##  #", "##  #", "# # #", "#  ##", "#  ##", "#   #"),
    'O' -> Vector(" ### ", "#   #", "#   #", "#   #", "#   #", "#   #", " ### "),
    'P' -> Vector("#### ", "#   #", "#   #", "#### ", "#    ", "#    ", "#    "),
    'Q' -> Vector(" ### ", "#   #", "#   #", "#   #", "# # #", "#  # ", " ## #"),
    'R' -> Vector("#### ", "#   #", "#   #", "#### ", "# #  ", "#  # ", "#   #"),
    'S' -> Vector(" ####", "#    ", "#    ", " ### ", "    #", "    #", "#### "),
    'T' -> Vector("#####", "  #  ", "  #  ", "  #  ", "  #  ", "  #  ", "  #  "),
    'U' -> Vector("#   #", "#   #", "#   #", "#   #", "#   #", "#   #", " ### "),
    'V' -> Vector("#   #", "#   #", "#   #", "#   #", "#   #", " # # ", "  #  "),
    'W' -> Vector("#   #", "#   #", "#   #", "# # #", "# # #", "## ##", "#   #"),
    'X' -> Vector("#   #", "#   #", " # # ", "  #  ", " # # ", "#   #", "#   #"),
    'Y' -> Vector("#   #", "#   #", " # # ", "  #  ", "  #  ", "  #  ", "  #  "),
    'Z' -> Vector("#####", "    #", "   # ", "  #  ", " #   ", "#    ", "#####"),
    '0' -> Vector(" ### ", "#   #", "#  ##", "# # #", "##  #", "#   #", " ### "),
    '1' -> Vector("  #  ", " ##  ", "  #  ", "  #  ", "  #  ", "  #  ", "#####"),
    '2' -> Vector(" ### ", "#   #", "    #", "   # ", "  #  ", " #   ", "#####"),
    '3' -> Vector(" ### ", "#   #", "    #", "  ## ", "    #", "#   #", " ### "),
    '4' -> Vector("   # ", "  ## ", " # # ", "#  # ", "#####", "   # ", "   # "),
    '5' -> Vector("#####", "#    ", "#### ", "    #", "    #", "#   #", " ### "),
    '6' -> Vector(" ### ", "#    ", "#    ", "#### ", "#   #", "#   #", " ### "),
    '7' -> Vector("#####", "    #", "   # ", "  #  ", " #   ", " #   ", " #   "),
    '8' -> Vector(" ### ", "#   #", "#   #", " ### ", "#   #", "#   #", " ### "),
    '9' -> Vector(" ### ", "#   #", "#   #", " ####", "    #", "    #", " ### "),
    '!' -> Vector("  #  ", "  #  ", "  #  ", "  #  ", "  #  ", "     ", "  #  "),
    '?' -> Vector(" ### ", "#   #", "    #", "   # ", "  #  ", "     ", "  #  "),
    ',' -> Vector("     ", "     ", "     ", "     ", "  ## ", "  ## ", " #   "),
    '.' -> Vector("     ", "     ", "     ", "     ", "     ", "  ## ", "  ## "),
    '-' -> Vector("     ", "     ", "     ", " ### ", "     ", "     ", "     "),
    '\'' -> Vector("  ## ", "  ## ", " #   ", "     ", "     ", "     ", "     "),
    ' ' -> Vector("     ", "     ", "     ", "     ", "     ", "     ", "     ")
  )

  def supports(c: Char): Boolean = glyphs.contains(c.toUpper)

  def glyph(c: Char): Vector[String] =
    glyphs.getOrElse(c.toUpper, glyphs('?'))

  /** Render text into a grid of booleans (true = lit pixel), with one blank
    * column between glyphs.
    */
  def render(text: String): Vector[Vector[Boolean]] =
    val gs = text.map(glyph)
    Vector.tabulate(Height) { row =>
      gs.iterator
        .map(g => g(row).map(_ == '#').toVector :+ false)
        .foldLeft(Vector.empty[Boolean])(_ ++ _)
        .dropRight(1)
    }
